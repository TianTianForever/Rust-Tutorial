// Rust provides  Higher Order Functions(HOF).
fn is_odd(n: u32) -> bool {
    n % 2 == 1
}

fn main() {
    println!("Find the sum of all squared odd numbers under 1000");
    let upper = 1000;
    // Imperative approach
    // Declare accumulator variable.
    let mut accumulator = 0;
    // Iterate: 0, 1, 2, 3, ... infinity.
    for n in 0.. {
        // Square the number.
        let n_squared = n * n;
        if n_squared >= upper {
            // Break loop if exceeded the upper limit.
            break;
        } else if is_odd(n_squared) {
            // Accumulate value. if it's odd.
            accumulator += n_squared;
        }
    }

    println!("imperative style: {}", accumulator);


    // Functional approach.
    let sum_of_squared_odd_numbers: u32 =
        (0..).map(|n| n * n)               // All natural numbers squared.
        .take_while(|&n| n < upper)        // Below upper limit.
        .filter(|&n| is_odd(n))            // That are odd.
        .fold(0, |sum, i| sum + i);        // Sum them.
    println!("functional style: {}", sum_of_squared_odd_numbers);
}
